package com.example.cst8334project.forms.util;

/**
 * An interface that defines the contract for various enums used for constructing Form objects.
 */
public interface FormEnum {

    /**
     * Get the String that represents the name of the {@link FormEnum} as it is displayed to be
     * displayed to the user and included in the CSV attachment.
     *
     * @return the formatted name of the {@link FormEnum}
     */
    String getName();
}
